function isEven(number){
    if(number % 2 === 0){
        return true;
    }
    return false;
}
  console.log(isEven(3));
  console.log(isEven(4));

  function isOdd(number){
    if(number % 2 === 1){
        return true;
    }
    return false;
  }
  console.log(isOdd(11));
  console.log(isOdd(4));
